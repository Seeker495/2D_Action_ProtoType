using System;

/*******************************************************************
 *  <概要>
 *  腹ペコに関連するアイテムの構造体。
 *******************************************************************/
[Serializable]
public struct FoodInfo
{
    /* アイテム名 */
    public string name;
    /*
     *  割合 +の場合は回復, -の場合は減少
     *  効果は腹ペコゲージの最大量に依存する。
     */
    public float healRatio;
}
